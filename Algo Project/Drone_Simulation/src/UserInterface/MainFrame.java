    public void timer() {
        Timer timer = new Timer();
        timer.scheduleAtFixedRate(new TimerTask() {
            @Override
            public void run() {
                /*method to delete old anomalies so that new anamolies can be created*/
                
                
                UtilityD.deleteAnomalies(drone.getNodeComponentsMap());
                UtilityD.createAnomalies(drone.getNodeComponentsMap());

                /* function to change the color of lable to indicate anomalies and traversed path*/
                for (Node node : drone.getNodeComponentsMap().values()) {
                    StringBuilder sb = new StringBuilder();
                    sb.append(node.getxCordinate());
                    sb.append(node.getyCordinate());
                    String s = sb.toString();
                    JLabel jlabel = (JLabel) getComponentByName(s);
                    UtilityD.setAnomalyColor(jlabel, node);         //color the node to inidcate the anomaly type.
                }
                if (searchPath != null) {
                    UtilityD.colorSearchPath(searchPath);
                }
                populateProgressBar();

                boolean flag = false;

                // searchPath is a path learned by machine and stored in hashmap. If the systemis running for the first time
                //this will be null. If this is null, it means system should calculate the path from source to destination and 
                // and must store this to the hashtable at the end.
                // if null the user distance algorithm
                if (searchPath == null) {
                    //System.out.println("current node" + drone.getCurrentNode());
                    //System.out.println("The insert flag is :" + insertPath);

                    flag = drone.shortestDistancePathAlgo();
                    String s = drone.getCurrentNode().getxCordinate() + drone.getCurrentNode().getyCordinate();
                    JLabel jlabel = (JLabel) getComponentByName(s);
                    jlabel.setForeground(Color.GREEN);

                    //traversedPath is a PathNode which contains ArrayList of nodes which store nodes travelled by drone in one run.
                    traversedPath.addNodeInNodesInPath(drone.getCurrentNode());

                    // At each node, searching if the path exists from that node till destination.
                    searchPath = getPathFromNewSource(drone.getCurrentNode());

                    insertPath = true;
                    nodeIndex = 0;

                    //smallPathList is a ArrayList of PathNodes. Each time an anomaly is faced, we use the distance formula to get adjacent node.
                    //Now from the new node, we need to capture the new path. So at each anomaly a new PathNode is created and in it
                    // we add current node. We need to add current node to all the PathNode while traveling.
                    if (!smallPathsList.isEmpty()) // if any small path exist
                    {
                        for (PathNode p : smallPathsList) {
                            p.addNodeInNodesInPath(drone.getCurrentNode());
                        }
                    }

                    System.err.println("Using distance formula to find new path as no predefined path exists. Psth followed : " + drone.getCurrentPath());
                    //System.out.println("Nearest node : " + drone.getCurrentNode().toString());

                    if (searchPath == null) // if no path available we have to add small path
                    {
                        PathNode smallPathNode = new PathNode();
                        PathLinkedList hashValue = new PathLinkedList();
                        hashValue.setFront(smallPathNode);
                        machineL.dataSet.put(drone.getCurrentNode(), hashValue);
                        smallPathsList.add(smallPathNode);
                        smallPathCount++;
                    }

                } // end of distance algo (nearest neighbour)
                //if searchPath has a path for given source and destination do below:
                else {
                  //  System.out.println("The size of searchPath is : " + searchPath.nodesInPath.size() + " and current index is " + nodeIndex);

                    System.out.println("SearchPath exists. Using existing path");

                    Node travel = searchPath.getNodesInPath().get(nodeIndex); //get single node from search path
                    if (travel.isAnomalyExists()) // anomaly at search path 
                    {
                        System.out.println("========================Anomaly at learned Path at node:  " + travel.toString() + " Again Using Distance algo================");
                        traversedPath.addNodeInNodesInPath(drone.getCurrentNode());

                        flag = drone.shortestDistancePathAlgo();
                        String s = drone.getCurrentNode().getxCordinate() + drone.getCurrentNode().getyCordinate();
                        JLabel jlabel = (JLabel) getComponentByName(s);
                        jlabel.setForeground(Color.GREEN);

                        if (!smallPathsList.isEmpty()) // if any small path exist
                        {
                            for (PathNode p : smallPathsList) {
                                p.addNodeInNodesInPath(drone.getCurrentNode());
                            }
                        }

                        System.out.println("Nearest node : " + drone.getCurrentNode().toString());

                        //Now form the new diverted node after anomaly, search if a stored path from diverted node to destination exists.
                        searchPath = getPathFromNewSource(drone.getCurrentNode());

                        if (searchPath == null) // if no path available we have to add small path
                        {
                            PathNode small = new PathNode();
                            small.addNodeInNodesInPath(drone.getCurrentNode());
                            PathLinkedList value = new PathLinkedList();
                            value.setFront(small);
                            machineL.dataSet.put(drone.getCurrentNode(), value);
                            smallPathsList.add(small);
                            smallPathCount++;
                        }
                        insertPath = true;
                        nodeIndex = 0;
                    } else // if searchPath has pasth, the travelling via trained path
                    {
                        if (travel.isLowIntesity()) {
                            drone.setFule(drone.getFule() - 3);
                        }

                        drone.setCurrentNode(travel);
                        traversedPath.addNodeInNodesInPath(travel);
                        JLabel jlabel = travel.getjLable();
                        jlabel.setForeground(Color.GREEN);
                        drone.setFule(drone.getFule() - 1);
                        nodeIndex++;

                        System.out.println(" Travelling via trained path. Learned Path:  " + travel.toString());

                         //check whether drone reaches destination or not. 
                        if (travel.equals(drone.getDestinationNode())) {
                            flag = true;
                        }

                        if (!smallPathsList.isEmpty()) // if any small path exist
                        {
                            for (PathNode p : smallPathsList) {
                                p.addNodeInNodesInPath(travel);
                            }
                        }

                    }

                }

                System.out.println("********Fuel remaining in drone :" + drone.getFule() + "*********");
                // the below code will run once the drone reaches destination.

                if (flag) {
                    if (!insertPath) // we dont have to insert any path as travelled via stored path
                    {
                        System.out.println("");
                        System.out.println("Travelled via learned path,thus node need to insert it to HashTable, only increase the confidence factor: " + searchPath);
                        searchPath.setConfidence(searchPath.getConfidence() + 1);
                        //  machineL.dataSet.get(drone.getSourceNode()).shuffle(searchPath);
                        System.out.println("-----------------------");

                    } else { // insert path
                        System.err.println("not travelled via learned path so inserting new path to hashtable: ");

                        traversedPath.setConfidence(traversedPath.getConfidence() + 1);

                        traversedPath.setDistance(traversedPath.getNodesInPath().size());

                        if (!smallPathsList.isEmpty()) {
                            for (PathNode p : smallPathsList) {
                                p.setDistance(p.getNodesInPath().size());
                                p.setConfidence(1);
                            }
                        }
                        if (machineL.dataSet.contains(drone.getSourceNode())) {
                            System.out.println("Adding new path to linked list of Source Node");
                            machineL.dataSet.get(drone.getSourceNode()).enqueue(traversedPath);
                        } else {
                            System.out.println("Adding new source to hashtable");
                            PathLinkedList value = new PathLinkedList();
                            value.setFront(traversedPath);
                            System.out.println("TraversedPath is " + traversedPath.nodesInPath);
                            machineL.dataSet.put(drone.getSourceNode(), value);
                            // System.out.println("if added? "+ MachineLearning.learning.containsKey(drone.getSource()));

                        }
                        System.out.println("Machin Set is " + machineL.dataSet.entrySet());
                    }
                    UtilityD.clearTraversedPath(drone.getNodeComponentsMap());
                    timeEnd = System.currentTimeMillis();
                    System.out.println("============================");

                    //System.out.println("Time take for this Run : "+(timeEnd-timeStart));
                    timer.cancel();

                    System.err.println("*************************************************************** ");
                    System.err.println("....................Drone Reached Destination................. ");
                    System.err.println(" ************************************************************** ");
                    // dB4OUtil.storeSystem(machineL);
                }

                drone.getCurrentNode().setIsTraversed(true);
            }

        }, 1000, 1000);

    }

    public PathNode getPathFromNewSource(Node n) {
        if (machineL.dataSet.isEmpty() || !machineL.dataSet.containsKey(n)) {
            insertPath = true;
            return null;
        } else {
            nodeIndex = 0;
            PathNode newSearchPath = machineL.dataSet.get(n).front;
            return newSearchPath;
        }
    }

